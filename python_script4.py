#!/usr/bin/python3
import sys
num = float(sys.argv[1])   # input variable from the command line

if num < 0:
  print("the number is negative")

elif num > 50:
  print("the number is larger than 50")
  if num%3 == 0:
    print("and the number is divisible by 3")
  else:
    print("but the number is not divisible by 3")

elif num < 50:
  print("the number is smaller than 50")
  if num%2 == 0:
    print("and the number is even")
  else:
    print("but the number is not even")
